package kr.co.psj;

/*
 
 */
public class Greeting{
    private final long id;
    private final String content;
    
    public Greeting(long id, String content){
        this.id = id;
        this.content = content;
    } // constructor
    
    public long getId(){
        return id;
    } // getId
    
    public String getContent(){
        return content;
    } // getContent
    
} // end class

























